    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        try {
            String client_email = emailofclint.getSelectedItem().toString();
            int samplenum = Integer.parseInt(numOfSample.getSelectedItem().toString());
            String analysisTypew = analysistypeCobox.getSelectedItem().toString();
            String enginner_email = EnginnersEmailsCobox.getSelectedItem().toString();
            if (!Regex3( client_email, enginner_email)) {

                Connection c2 = FastCon.DBConnect();
                String plsql = "{CALL simple_work(?,?,?,?)}";
                CallableStatement cs = c2.prepareCall(plsql);

                cs.setString(1, client_email);
                cs.setInt(2, samplenum);
                cs.setString(3, analysisTypew);
                cs.setString(4, enginner_email);

                int a = cs.executeUpdate();
                if (a == 1) {
                    JOptionPane.showMessageDialog(this, "Data Inserted Successfully");
                emailofclint.setSelectedIndex(-1);
 numOfSample.setSelectedIndex(-1);
                    analysistypeCobox.setSelectedIndex(-1);
                } else {
                    JOptionPane.showMessageDialog(this, "Data Didn't insert ");

                }
                cs.close();
                c2.close();
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Data");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());

        }

    }//GEN-LAST:event_jButton10ActionPerformed

